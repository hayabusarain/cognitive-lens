import { COMPATIBILITY } from "./compatibility";
import { TYPE_INFO } from "./type-info";

export type RelationshipTone = "synergy" | "growth" | "friction" | "collapse";

export interface TimelineEntry {
  day: string;
  title: string;
  event: string;
  tone: RelationshipTone;
}

function getArchetype(a: string, b: string): "dual" | "synergy" | "neutral" | "friction" | "clash" {
  if (a === b) return "dual";
  const compat = COMPATIBILITY[a as keyof typeof COMPATIBILITY];
  if (compat?.bestPartner.type === b) return "synergy";
  if (compat?.hardestMatch.type === b) return "clash";

  // Count letter matches
  const matches = [...a].filter((c, i) => c === b[i]).length;
  if (matches >= 3) return "neutral";
  return "friction";
}

function typeLabel(t: string): string {
  return `${t}（${TYPE_INFO[t]?.name ?? t}）`;
}

export function generateRelationshipTimeline(
  typeA: string,
  typeB: string
): TimelineEntry[] {
  const arch = getArchetype(typeA, typeB);
  const a = typeLabel(typeA);
  const b = typeLabel(typeB);

  const infoA = TYPE_INFO[typeA];
  const infoB = TYPE_INFO[typeB];

  // Dimension analysis
  const sameEI = typeA[0] === typeB[0];
  const sameSN = typeA[1] === typeB[1];
  const sameTF = typeA[2] === typeB[2];
  const sameJP = typeA[3] === typeB[3];

  const eiNote = sameEI
    ? `二人とも${typeA[0] === "E" ? "外向" : "内向"}型。エネルギーの充電スタイルがそっくりで、「一緒にいる」と「一人になる」のタイミングが自然と合う`
    : `片方は人といると元気になるタイプ、もう片方は一人で充電するタイプ。「もっとそばにいてほしい」vs「少し距離がほしい」の温度差が、じわじわ積み重なる`;

  const snNote = sameSN
    ? `話の"解像度"が同じ。「それって具体的にどういうこと？」「つまり何が言いたいの？」みたいな嚙み合わなさが起きにくい`
    : `一方は「で、結局どうするの？」と現実的なことを聞きたいのに、もう一方は「そもそも本質的には…」と話を広げる。このすれ違いが一番しんどい`;

  const tfNote = sameTF
    ? `「正しいかどうか」か「嫌な気持ちにならないか」か、判断の軸が揃っている。もめたときに話し合いが通じやすい`
    : `片方は「事実とロジック」で話し、もう片方は「気持ちと関係性」で話す。同じ出来事を見ても、まったく違うことを問題だと感じる`;

  const jpNote = sameJP
    ? `生活リズムの「ガチガチ度」が似てる。計画の立て方や、ぐだぐだする許容度が同じくらいで、細かいストレスが少ない`
    : `一方は「決めたことは守ろうよ」派、もう一方は「もっと流れに任せよう」派。旅行の計画とか、家事の進め方で、小さなバトルが起きやすい`;

  switch (arch) {
    case "dual":
      return [
        {
          day: "Day 1",
          title: "「この人、なんか分かる」",
          event: `${a}と${b}、同じタイプ同士の出会い。話してみたら、言葉の選び方も、ちょっとした間の取り方も、自分とそっくり。「なんか知らないけど、この人と話すのラクだな」って感覚が生まれる。でもそれ、ちょっと怖くもある。自分が隠してきた部分まで、全部バレそうな感じがするから。`,
          tone: "synergy",
        },
        {
          day: "Day 30",
          title: "「自分の嫌いな部分」を相手に見てしまう",
          event: `1ヶ月後、相手の欠点が見えてくる。でもそれ、自分の欠点とまったく同じ形をしてる。自分が一番気にしているクセを、相手がそのままやってくる。「イライラする…」と思った瞬間、「あ、これ自分もやってるやつだ」って気づく、ちょっと苦い体験が始まる。`,
          tone: "growth",
        },
        {
          day: "Day 100",
          title: "「いや、俺の方が正しい」の地獄ループ",
          event: `同じ発想で同じ問題に向かう二人。うまくいくときは最強だけど、意見がぶつかると誰も引かない。${tfNote}。「自分のやり方が正しい」という確信の根拠が同じだから、話し合いが平行線をたどる。この時期に「どう折り合いをつけるか」を見つけられるかどうかで、この関係の未来が決まる。`,
          tone: "friction",
        },
        {
          day: "Day 365",
          title: "最高の仲間か、静かなお別れか",
          event: `1年後、この関係は二つに分かれている。互いの内側を知り尽くした上で、それでも一緒にいることを選ぶ深い絆か。それとも「もう自分の鏡は見たくない」という、穏やかな距離感か。どちらに転んでも、この関係で得たものはある。相手は、あなたが一番向き合いたくなかった「自分自身」を、正直に見せてくれた人だから。`,
          tone: "growth",
        },
      ];

    case "synergy":
      return [
        {
          day: "Day 1",
          title: "「なんで？」って聞いてないのに、なんか分かる",
          event: `${a}と${b}の出会い。${eiNote}。見た目は全然違うのに、話してると「あ、なんか通じてる」という感覚がある。「この人と話すと、ずっとモヤモヤしてたことに言葉がつく」という、ちょっと不思議な体験が始まる。`,
          tone: "synergy",
        },
        {
          day: "Day 30",
          title: "「得意なことが逆」で、なぜかうまく回る",
          event: `${snNote}。最初はこの「ズレ」が新鮮な刺激になる。一方が気づかないことを、もう一方がさらっと補う。でも気をつけて。いつも「与えてる」と感じる方が、少しずつ疲れ始める時期でもある。「私ばっかり…」という気持ちは、ちゃんと声に出しておかないと後で爆発する。`,
          tone: "growth",
        },
        {
          day: "Day 100",
          title: "「違い」が武器になった",
          event: `${tfNote}。3ヶ月後、二人の「差」はもう問題じゃない。むしろ役割分担として機能し始めてる。一人だったら気づけなかったことに、二人だと一瞬で気づける。「この人なしで、どうやって生きてたんだろ」と思い始めるのは、このくらいの時期から。`,
          tone: "synergy",
        },
        {
          day: "Day 365",
          title: "言わなくても分かる、でも言い続ける",
          event: `1年後、二人は互いの考え方を「もう一個の自分の頭」として使いこなせるようになっている。${jpNote}。ぶつかることはまだある。でもその摩擦が、この関係をよりよくする燃料になってる。互いを「変えよう」とせず、「そういう人だ」と受け入れながら好きでいられる。それがこの組み合わせの一番の強みだ。`,
          tone: "synergy",
        },
      ];

    case "clash":
      return [
        {
          day: "Day 1",
          title: "「なんか、合わないかも」",
          event: `${a}と${b}、最初の会話から「なんか変だな」という感覚がある。${eiNote}。話すテンポ、沈黙の感じ方、何に反応するか——根本から噛み合わない。どちらも悪くない。ただ、互いの「当たり前」がまったく別の形をしているだけ。それが分かるのは、もう少し後の話だ。`,
          tone: "friction",
        },
        {
          day: "Day 30",
          title: "「なんで分かってくれないの？」「だから理屈で言えよ」の地獄ループ",
          event: `${snNote}。気を許し始めると、本音がぶつかり始める。${tfNote}。二人が同時に「この人、分かってくれない」と感じる瞬間がある。でも実は、どちらも間違ってない。「何を正しいと呼ぶか」の定義が、根っこから違うだけ。それが一番しんどい。`,
          tone: "friction",
        },
        {
          day: "Day 100",
          title: "ぶつかるより、もう距離を置く方がラク",
          event: `${jpNote}。3ヶ月後、表向きのケンカは減る。でも仲直りしたわけじゃない。「この話題は触れない」「ここには踏み込まない」という暗黙のルールが増えていく。二人とも、あのバトルより今の静けさの方が、ずっと寂しいって気づいてる。`,
          tone: "collapse",
        },
        {
          day: "Day 365",
          title: "それでも、なぜか離れられない",
          event: `1年後、この関係がまだ続いているとしたら、互いへの期待をずいぶん下げた結果だ。完全に分かり合えることはない。でも、ここで聞きたい。傷つけ合いながらも一緒にいる二人は、相手の中に何を見てるんだろう。憎しみじゃなくて、自分がずっと手放せなかった何か——への、曲がった形の執着かもしれない。`,
          tone: "friction",
        },
      ];

    case "friction":
      return [
        {
          day: "Day 1",
          title: "「なんか、ちょっと違うな」",
          event: `${a}と${b}の出会い。ドキドキもなければ、拒否感もない。でも話していると、相手の言葉の選び方や反応の仕方に、小さな「ん？」が積み重なっていく。「おかしい」じゃなくて、「自分とは違う」という、静かな違和感として。${eiNote}。`,
          tone: "growth",
        },
        {
          day: "Day 30",
          title: "「この人、別の星の人みたい」",
          event: `${snNote}。1ヶ月後、相手が自分とはまったく違う「言語」で世界を見てることが分かってくる。${tfNote}。どっちが正しいとかじゃない。「正しいって何？」の定義が、根本からずれてる。これを「面白い」と思えるか、「めんどくさい」と思うかで、この先が変わってくる。`,
          tone: "friction",
        },
        {
          day: "Day 100",
          title: "「理解しよう」とするか、「諦めよう」とするか",
          event: `${jpNote}。3ヶ月後、この関係は岐路に立つ。「この人はこういう人なんだ」と受け入れて、違いを面白がれるなら、二人ともひと回り大きくなれる。でも「やっぱり分かり合えない」と固めてしまえば、形だけのやり取りに縮んでいく。どっちに進むかは、能力じゃなくて意志の問題だ。`,
          tone: "friction",
        },
        {
          day: "Day 365",
          title: "「全部は分からなくても、ここにいる」",
          event: `1年後、この二人はお互いを完全には理解できていない。でも、分からないまま向き合うことを選んでいる。完璧に通じ合えなくても、「それでも一緒にいたい」って思える関係は、意外と少ない。その不完全さを丸ごと受け入れてること自体が、この関係の誠実さだ。`,
          tone: "growth",
        },
      ];

    default: // neutral
      return [
        {
          day: "Day 1",
          title: "「話しやすいな」、それだけ",
          event: `${a}と${b}の出会い。ドラマチックな一目惚れも、謎の拒否感もない。でも話していると、自然にテンポが合う。共通点と違いがいい感じに混ざって、会話が詰まらない。${eiNote}。この「普通に居心地いい」感覚こそが、のちの信頼の土台になる。`,
          tone: "growth",
        },
        {
          day: "Day 30",
          title: "「お、こういう人か」",
          event: `${snNote}。1ヶ月後、相手の「クセ」が見えてくる。${tfNote}。ぶつかることはあっても、大きなバトルにはならない。それよりも「そういう考え方するんだ、面白いな」という発見の方が多い。自然に相手のリズムに合わせ始めてて、気づいたら信頼が積み上がってる。`,
          tone: "growth",
        },
        {
          day: "Day 100",
          title: "ドラマはないけど、ちゃんとある",
          event: `${jpNote}。3ヶ月後、この関係は「いい感じ」に落ち着いてる。感動的な出来事もない、派手なケンカもない。でもそれって、すごいことだと思う。「なんとなく今日も一緒にいたい」と思える人は、意外と少ない。この「平凡な安定」を過小評価しちゃダメだ。`,
          tone: "synergy",
        },
        {
          day: "Day 365",
          title: "「いて当たり前」が、一番強い",
          event: `1年後、この関係は静かに続いてる。劇的な物語はない。でも「あの人ならいてくれる」という確信は、他のどの組み合わせより深く根を張ってる。嵐に強い関係もいい。でもこの二人の強さは、嵐を必要としないところだ。`,
          tone: "synergy",
        },
      ];
  }
}
